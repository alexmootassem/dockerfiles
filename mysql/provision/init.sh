if [ ! -d ${MYSQL_VOLUME}/mysql ]; then
    # Setup Mysql data directory
    mysql_install_db --datadir=${MYSQL_VOLUME}
fi

set +e

RET=1
while [[ RET -ne 0 ]]; do
    sleep 1;
    pgrep -f $(which mysqld_safe) > /dev/null 2>&1; RET=$?
done

